# –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –±–æ—Ç –≤—Å–µ –µ—â–µ —Ä–∞–±–æ—Ç–∞–µ—Ç
if ! kill -0 $BOT_PID 2>/dev/null; then
    echo "‚ùå –ë–æ—Ç –∑–∞–≤–µ—Ä—à–∏–ª—Å—è –≤–æ –≤—Ä–µ–º—è –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏"
    echo "üìã –ü–æ—Å–ª–µ–¥–Ω–∏–µ —Å—Ç—Ä–æ–∫–∏ –ª–æ–≥–∞ –±–æ—Ç–∞:"
    tail -10 /tmp/logs/bot.log
    exit 1
fi

# –ó–∞–ø—É—Å–∫–∞–µ–º –∞–¥–º–∏–Ω-–ø–∞–Ω–µ–ª—å –≤ —Ñ–æ–Ω–µ —Å –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ–º
echo "üåê –ó–∞–ø—É—Å–∫ –∞–¥–º–∏–Ω-–ø–∞–Ω–µ–ª–∏..."
python run_admin.py > /tmp/logs/admin.log 2>&1 &
ADMIN_PID=$!

if [ $? -eq 0 ]; then
    echo "‚úÖ –ê–¥–º–∏–Ω-–ø–∞–Ω–µ–ª—å –∑–∞–ø—É—â–µ–Ω–∞ (PID: $ADMIN_PID)"
else
    echo "‚ùå –û—à–∏–±–∫–∞ –∑–∞–ø—É—Å–∫–∞ –∞–¥–º–∏–Ω-–ø–∞–Ω–µ–ª–∏"
    kill $BOT_PID 2>/dev/null
    exit 1
fi

# –ñ–¥–µ–º –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ –∞–¥–º–∏–Ω-–ø–∞–Ω–µ–ª–∏
echo "‚è≥ –û–∂–∏–¥–∞–Ω–∏–µ –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏ –∞–¥–º–∏–Ω-–ø–∞–Ω–µ–ª–∏..."
sleep 10

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –∞–¥–º–∏–Ω-–ø–∞–Ω–µ–ª—å —Ä–∞–±–æ—Ç–∞–µ—Ç
if ! kill -0 $ADMIN_PID 2>/dev/null; then
    echo "‚ùå –ê–¥–º–∏–Ω-–ø–∞–Ω–µ–ª—å –∑–∞–≤–µ—Ä—à–∏–ª–∞—Å—å –≤–æ –≤—Ä–µ–º—è –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏–∏"
    echo "üìã –ü–æ—Å–ª–µ–¥–Ω–∏–µ —Å—Ç—Ä–æ–∫–∏ –ª–æ–≥–∞ –∞–¥–º–∏–Ω-–ø–∞–Ω–µ–ª–∏:"
    tail -10 /tmp/logs/admin.log
    kill $BOT_PID 2>/dev/null
    exit 1
fi

echo "üéâ –í—Å–µ —Å–µ—Ä–≤–∏—Å—ã –∑–∞–ø—É—â–µ–Ω—ã —É—Å–ø–µ—à–Ω–æ!"
echo "ü§ñ Telegram –±–æ—Ç: PID $BOT_PID"
echo "üåê –ê–¥–º–∏–Ω-–ø–∞–Ω–µ–ª—å: PID $ADMIN_PID"
echo "üåê –ê–¥–º–∏–Ω-–ø–∞–Ω–µ–ª—å –¥–æ—Å—Ç—É–ø–Ω–∞: http://0.0.0.0:8080"
echo "üìã –õ–æ–≥–∏ –±–æ—Ç–∞: /tmp/logs/bot.log"
echo "üìã –õ–æ–≥–∏ –∞–¥–º–∏–Ω-–ø–∞–Ω–µ–ª–∏: /tmp/logs/admin.log"
echo "üìä –ú–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥ –∞–∫—Ç–∏–≤–µ–Ω..."

# –ó–∞–ø—É—Å–∫–∞–µ–º –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥ –≤ —Ñ–æ–Ω–µ
monitor_process $BOT_PID "Telegram –±–æ—Ç" &
monitor_process $ADMIN_PID "–ê–¥–º–∏–Ω-–ø–∞–Ω–µ–ª—å" &

# –û—Å–Ω–æ–≤–Ω–æ–π —Ü–∏–∫–ª –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞
while true; do
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—Ç–∞—Ç—É—Å –±–æ—Ç–∞
    if ! kill -0 $BOT_PID 2>/dev/null; then
        echo "‚ùå –ë–æ—Ç –∑–∞–≤–µ—Ä—à–∏–ª—Å—è!"
        echo "üìã –ü–æ—Å–ª–µ–¥–Ω–∏–µ —Å—Ç—Ä–æ–∫–∏ –ª–æ–≥–∞ –±–æ—Ç–∞:"
        tail -10 /tmp/logs/bot.log
        break
    fi
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—Ç–∞—Ç—É—Å –∞–¥–º–∏–Ω-–ø–∞–Ω–µ–ª–∏
    if ! kill -0 $ADMIN_PID 2>/dev/null; then
        echo "‚ùå –ê–¥–º–∏–Ω-–ø–∞–Ω–µ–ª—å –∑–∞–≤–µ—Ä—à–∏–ª–∞—Å—å!"
        echo "üìã –ü–æ—Å–ª–µ–¥–Ω–∏–µ —Å—Ç—Ä–æ–∫–∏ –ª–æ–≥–∞ –∞–¥–º–∏–Ω-–ø–∞–Ω–µ–ª–∏:"
        tail -10 /tmp/logs/admin.log
        break
    fi
    
    # –í—ã–≤–æ–¥–∏–º —Å—Ç–∞—Ç—É—Å –∫–∞–∂–¥—ã–µ 60 —Å–µ–∫—É–Ω–¥
    sleep 60
    echo "‚úÖ $(date): –í—Å–µ —Å–µ—Ä–≤–∏—Å—ã —Ä–∞–±–æ—Ç–∞—é—Ç (Bot: $BOT_PID, Admin: $ADMIN_PID)"
