async def verify_database_health():
    """–ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–¥–æ—Ä–æ–≤—å—è –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö"""
    
    logger.info("üè• –ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–¥–æ—Ä–æ–≤—å—è –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö...")
    
    try:
        db = Database()
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º –æ—Å–Ω–æ–≤–Ω—ã–µ —Ç–∞–±–ª–∏—Ü—ã
        tables_to_check = [
            'users', 'subscriptions', 'requests', 
            'payments', 'broadcasts'
        ]
        
        for table in tables_to_check:
            try:
                # –ü—Ä–æ—Å—Ç–æ–π –∑–∞–ø—Ä–æ—Å –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —Ç–∞–±–ª–∏—Ü—ã
                if table == 'users':
                    count = await db.get_users_count()
                elif table == 'subscriptions':
                    count = await db.get_active_subscribers_count()
                elif table == 'requests':
                    count = len(await db.get_user_requests(1, limit=1))
                else:
                    count = 0  # –î–ª—è –¥—Ä—É–≥–∏—Ö —Ç–∞–±–ª–∏—Ü
                
                logger.info(f"‚úÖ –¢–∞–±–ª–∏—Ü–∞ {table}: OK (–∑–∞–ø–∏—Å–µ–π: {count})")
                
            except Exception as e:
                logger.warning(f"‚ö†Ô∏è –ü—Ä–æ–±–ª–µ–º–∞ —Å —Ç–∞–±–ª–∏—Ü–µ–π {table}: {e}")
        
        logger.info("‚úÖ –ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–¥–æ—Ä–æ–≤—å—è –∑–∞–≤–µ—Ä—à–µ–Ω–∞")
        
    except Exception as e:
        logger.error(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–æ–≤–µ—Ä–∫–∏ –∑–¥–æ—Ä–æ–≤—å—è: {e}")

